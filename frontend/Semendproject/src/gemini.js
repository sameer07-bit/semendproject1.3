// 3. Grammar and Style suggestions
export async function checkGrammarAndStyle(content) {
  const plainText = getPlainText(content);
  if (!plainText) return "Write some text first to run a grammar scan.";
  
  try {
    const prompt = `Review the following text for spelling, grammar errors, and style issues. If there are errors, return a concise bulleted list explaining what to fix and how. If the text is perfect, write "Everything looks excellent! No corrections needed."\n\nText:\n${plainText.substring(0, 3000)}`;
    const result = await askGemini(prompt);
    if (!result || result.includes("AI assistant unavailable") || result.includes("Gemini API error")) {
      throw new Error("Gemini API limit or error");
    }
    return result;
  } catch (error) {
    console.warn("Using local fallback for Grammar check:", error);
    const errors = [];
    if (plainText.includes("  ")) {
      errors.push("• Style: Found double spacing between words. Consider formatting to single spacing.");
    }
    // Check if sentences start with capital letters
    const sentences = plainText.split(/[.!?]\s+/);
    let capitalizedError = false;
    for (let s of sentences) {
      if (s && s[0] !== s[0].toUpperCase()) {
        capitalizedError = true;
        break;
      }
    }
    if (capitalizedError) {
      errors.push("• Grammar: Ensure all sentences start with a capital letter.");
    }
    
    // Spellcheck common words
    const commonMisspells = {
      "teh": "the",
      "recieve": "receive",
      "dont": "don't",
      "im": "I'm",
      "wont": "won't",
      "aboutt": "about"
    };
    const words = plainText.toLowerCase().split(/\s+/);
    for (let w of words) {
      const cleanW = w.replace(/[^a-z]/g, "");
      if (commonMisspells[cleanW]) {
        errors.push(`• Spelling: Potential typo "${cleanW}". Did you mean "${commonMisspells[cleanW]}"?`);
      }
    }
    
    if (errors.length === 0) {
      return "Everything looks excellent! Local scan found no formatting or capitalization issues. (Offline fallback active)";
    }
    return "Grammar & Style Scan Results (Local Fallback):\n" + errors.join("\n");
  }
}

// 4. Article Summarization
export async function summarizeArticleText(content) {
  const plainText = getPlainText(content);
  if (!plainText) return "No text available to summarize.";
  
  try {
    const prompt = `Summarize the following text in exactly one or two powerful sentences, suitable as a reading preview. Return ONLY the summary sentences, nothing else.\n\nText:\n${plainText.substring(0, 3000)}`;
    const result = await askGemini(prompt);
    if (!result || result.includes("AI assistant unavailable") || result.includes("Gemini API error")) {
      throw new Error("Gemini API limit or error");
    }
    return result;
  } catch (error) {
    console.warn("Using local fallback for Summary:", error);
    const sentences = plainText.split(/[.!?]/).filter(s => s.trim().length > 5);
    const summaryBase = sentences[0] ? sentences[0].trim() : plainText.substring(0, 100);
    return `${summaryBase}. (A reflective exploration on thoughts, structure, and community narratives.)`;
  }
}

// 5. Generate Keywords for Cover Photo
export async function generateCoverKeywords(title, content) {
  const plainText = getPlainText(content);
  const context = `Title: ${title}\nContent Preview: ${plainText ? plainText.substring(0, 500) : ""}`;
  const prompt = `Given the following article details, output exactly 2 simple keywords (comma-separated, e.g. "writing, desk" or "nature, forest") that represent a matching background scene for this article. Output ONLY the two words, no punctuation or explanation.\n\n${context}`;
  
  try {
    const keywords = await askGemini(prompt);
    if (!keywords || keywords.includes("AI assistant unavailable") || keywords.includes("Gemini API error")) {
      throw new Error("Gemini API limit or error");
    }
    return keywords.replace(/[^a-zA-Z0-9,\s]/g, "").trim() || "writing,book";
  } catch (error) {
    console.warn("Using local fallback for cover keywords:", error);
    const titleWords = (title || "")
      .toLowerCase()
      .replace(/[^a-z\s]/g, "")
      .split(/\s+/)
      .filter(w => w.length > 3 && !["with", "from", "that", "this", "your", "their", "about", "here", "there"].includes(w));
    return titleWords.slice(0, 2).join(",") || "writing";
  }
}
