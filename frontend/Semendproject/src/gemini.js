function getApiKey() {
  const storedKey = localStorage.getItem("gemini_api_key");
  if (storedKey && storedKey.trim() !== "" && !storedKey.includes("DUMMY_KEY")) {
    return storedKey.trim();
  }
  return import.meta.env.VITE_GEMINI_API_KEY || "YOUR_GEMINI_API_KEY";
}

async function askGemini(promptText) {
  const apiKey = getApiKey();
  const apiURL = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${apiKey}`;

  try {
    const response = await fetch(apiURL, {
      method: "POST",
      headers: {
        "Content-Type": "application/json"
      },
      body: JSON.stringify({
        contents: [
          {
            parts: [
              {
                text: promptText
              }
            ]
          }
        ]
      })
    });

    if (!response.ok) {
      throw new Error(`Gemini API error: ${response.status} ${response.statusText}`);
    }

    const data = await response.json();
    if (data.candidates && data.candidates[0] && data.candidates[0].content && data.candidates[0].content.parts[0]) {
      return data.candidates[0].content.parts[0].text.trim();
    }
    return "No suggestions generated by AI.";
  } catch (error) {
    console.error("Error communicating with Gemini AI:", error);
    return `AI assistant unavailable: ${error.message}`;
  }
}

// Helper to clean HTML tags and trim
function getPlainText(html) {
  if (!html) return "";
  return html.replace(/<[^>]*>/g, "").replace(/&nbsp;/g, " ").trim();
}

// Helper for random selection
function getRandomElement(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

// 1. Headline / Title Generator
export async function generateHeadlineSuggestions(content, category = "Essay") {
  const plainText = getPlainText(content);
  if (!plainText) return ["Draft is empty. Type some content first."];
  
  try {
    const prompt = `Based on the following article content, generate 3 unique, catchy, and professional headlines or titles. Return ONLY the titles in a numbered list (e.g., "1. Title One\\n2. Title Two\\n3. Title Three") without any conversational introduction or explanation.\n\nContent:\n${plainText.substring(0, 3000)}`;
    const result = await askGemini(prompt);
    if (!result || result.includes("AI assistant unavailable") || result.includes("Gemini API error")) {
      throw new Error("Gemini API limit or error");
    }
    return result
      .split("\n")
      .map(line => line.replace(/^\d+\.\s*/, "").trim())
      .filter(line => line.length > 0);
  } catch (error) {
    console.warn("Using local fallback for Title Suggestions:", error);
    const words = plainText.split(/\s+/).slice(0, 4).join(" ");
    const cat = (category || "Essay").toLowerCase();
    
    if (cat.includes("poetry")) {
      return [
        `Whispers of the Soul: ${words}...`,
        `The Echoing Verse`,
        `Starlight and Silences`
      ];
    } else if (cat.includes("fiction")) {
      return [
        `The Mystery of "${words}..."`,
        `A Twist of Fate`,
        `Beyond the Threshold`
      ];
    } else if (cat.includes("journal")) {
      return [
        `Daily Reflections: ${words}...`,
        `Moments of Clarity`,
        `The Inner Canvas`
      ];
    } else {
      return [
        `Reflections on "${words}..."`,
        `The Silent Chronicle: A Study in Expression`,
        `Exploring the Depths of Personal Narrative`
      ];
    }
  }
}

